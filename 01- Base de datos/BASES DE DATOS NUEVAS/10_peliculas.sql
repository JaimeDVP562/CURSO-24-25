-- Eliminar la base de datos peliculas si existe
IF EXISTS (SELECT * FROM sys.databases WHERE name = 'peliculas')
BEGIN
    DROP DATABASE peliculas;
END;
GO

-- Crear y usar la base de datos peliculas
CREATE DATABASE peliculas;
GO

USE peliculas;
GO

-- Creación de la tabla actor
CREATE TABLE actor (
    act_id INT,
    act_nombre NVARCHAR(20) NOT NULL,
    act_apellidos NVARCHAR(20) NOT NULL,
    act_sexo CHAR(1) NOT NULL,
    CONSTRAINT pk_actor PRIMARY KEY (act_id)
);
GO

-- Creación de la tabla director
CREATE TABLE director (
    dir_id INT,
    dir_nombre NVARCHAR(20) NOT NULL,
    dir_apellidos NVARCHAR(20) NOT NULL,
    CONSTRAINT pk_director PRIMARY KEY (dir_id)
);
GO

-- Creación de la tabla genero
CREATE TABLE genero (
    gen_id INT,
    gen_titulo NVARCHAR(20) NOT NULL,
    CONSTRAINT pk_genero PRIMARY KEY (gen_id)
);
GO

-- Creación de la tabla critico
CREATE TABLE critico (
    cri_id INT,
    cri_nombre NVARCHAR(30) NOT NULL,
    CONSTRAINT pk_critico PRIMARY KEY (cri_id)
);
GO

-- Creación de la tabla pelicula
CREATE TABLE pelicula (
    peli_id INT,
    peli_titulo NVARCHAR(50) NOT NULL,
    peli_anio INT NOT NULL,
    peli_duracion INT NOT NULL,
    peli_idioma NVARCHAR(50) NOT NULL,
    peli_estreno DATE,
    peli_pais NVARCHAR(50) NOT NULL,
    CONSTRAINT pk_pelicula PRIMARY KEY (peli_id)
);
GO

-- Creación de la tabla peli_dir
CREATE TABLE peli_dir (
    dir_id INT,
    peli_id INT,
    CONSTRAINT pk_peli_dir PRIMARY KEY (dir_id, peli_id),
    CONSTRAINT fk_peli_dir_director FOREIGN KEY (dir_id) REFERENCES director (dir_id),
    CONSTRAINT fk_peli_dir_pelicula FOREIGN KEY (peli_id) REFERENCES pelicula (peli_id)
);
GO

-- Creación de la tabla peli_elenco
CREATE TABLE peli_elenco (
    act_id INT,
    peli_id INT,
    papel NVARCHAR(30) NOT NULL,
    CONSTRAINT pk_peli_elenco PRIMARY KEY (act_id, peli_id),
    CONSTRAINT fk_peli_elenco_actor FOREIGN KEY (act_id) REFERENCES actor (act_id),
    CONSTRAINT fk_peli_elenco_pelicula FOREIGN KEY (peli_id) REFERENCES pelicula (peli_id)
);
GO

-- Creación de la tabla peli_genero
CREATE TABLE peli_genero (
    gen_id INT,
    peli_id INT,
    CONSTRAINT pk_peli_genero PRIMARY KEY (gen_id, peli_id),
    CONSTRAINT fk_peli_genero_genero FOREIGN KEY (gen_id) REFERENCES genero (gen_id),
    CONSTRAINT fk_peli_genero_pelicula FOREIGN KEY (peli_id) REFERENCES pelicula (peli_id)
);
GO

-- Creación de la tabla valoracion
CREATE TABLE valoracion (
    peli_id INT,
    cri_id INT,
    val_estrellas INT,
    val_cantidad INT,
    CONSTRAINT pk_valoracion PRIMARY KEY (peli_id, cri_id),
    CONSTRAINT fk_valoracion_pelicula FOREIGN KEY (peli_id) REFERENCES pelicula (peli_id),
    CONSTRAINT fk_valoracion_critico FOREIGN KEY (cri_id) REFERENCES critico (cri_id)
);
GO

